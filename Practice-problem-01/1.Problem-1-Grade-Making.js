var n = 90;

if(n < 33)
    console.log("F");

else if(n >= 33 && n < 40)
    console.log("D");

else if(n >= 40 && n < 50)
    console.log("C");

else if(n >= 50 && n < 60)
    console.log("B");

else if(n >= 60 && n < 70)
    console.log("A-");

else if(n >= 70 && n < 80)
    console.log("A");

else
    console.log("A+");

